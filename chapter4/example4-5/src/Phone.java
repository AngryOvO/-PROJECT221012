public class Phone
{
    String name;
    String tel;

    public Phone()
    {

    }

    public Phone(String name, String tel)
    {
        this.name = name;
        this.tel = tel;
    }

    public String returnname()
    {
        return this.name;
    }

    public String returntel()
    {
        return this.tel;
    }

}
